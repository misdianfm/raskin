                        <a data-toggle="collapse" href="#delapan" aria-expanded="false" aria-controls="delapan" data-target="#delapan" style="text-align: center;">
                            <h4>Apa itu kriteria, variabel dan bobot?</h4>
                        </a>
                        <div class="collapse" id="delapan" aria-expanded="false" style="text-align: justify;">
                            <hr>
                            <b>Kriteria</b><br>
                            Dalam SPK Penerima Bantuan Raskin terdapat 15 kriteria, salah satunya adalah Jenis Lantai Tempat Tinggal Terluas.<br>

                            <br><b>Variabel</b><br>
                            Variabel merupakan nilai yang terdapat pada kriteria. Variabel pada satu kriteria dapat terdiri dari satu maupun lebih dari satu.<br>
                            Misalnya, variabel dari Kriteria Jenis Lantai Tempat Tinggal Terluas adalah : <br>
                            - Tanah<br>
                            - Semen<br>
                            - Pur/Tegel<br>
                            - Keramik<br>
                            - Marmer<br>

                            <br><b>Bobot</b><br>
                            Bobot merupakan nilai berupa angka yang terdapat dalam suatu variabel maupun kriteria.<br>
                            Misalnya, bobot dari variabel Tanah adalah 5, bobot dari variabel Semen adalah 4.
                        </div>
