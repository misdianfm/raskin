                            <h4>Bagaimana bila lupa kata sandi untuk <i>login</i> ke dalam sistem SPK Penerima Bantuan Raskin?</h4>
                        </a>
                        <div class="collapse" id="empat" aria-expanded="false" style="text-align: justify;">
                            <hr>
                            <b>Cara 1:</b><br>
                            1. Akses menu <i>login</i> pada pojok kanan atas.<br>
                            2. Setelah berhasil mengakses menu <i>login</i>, akan muncul halaman login.<br>
                            3. Pada halaman <i>login</i>, terdapat link lupa <i>password</i>. Tekan link tersebut. Jika berhasil, maka sistem akan menampilkan halaman lupa <i>password</i>.<br>
                            4. Isi form lupa password tersebut dengan alamat email yang telah terdaftar pada sistem. Lalu tekan tombol reset password.<br>
                            5. Kemudian cek email untuk mendapatkan link reset password. Tekan link tersebut.<br>
                            6. Link tersebut akan mengarahkan pengguna ke halaman reset password. Lalu isi form reset password dengan password baru.<br>
                            7. Jika berhasil, maka pengguna dapat masuk dengan password baru.<br><br>

                            <b>Cara 2:</b><br>
                            1. Hubungi Superadmin untuk meminta reset password dari dalam sistem.
